      <h1>How To Build An Ecosystem Strategy</h1>
      <p>By 1980, IBM had hit an inflection point. It had already missed out on the minicomputer <i>revolution. Now, with new, even</i> smaller “microcomputers” evolving into a growing market, it was about to miss out again. So it set up a “skunk works” in Boca Raton, FL and, in less than a year, launched the IBM PC.</p>
      <p>Today, at the end of the digital revolution, IBM is at a similar juncture. Yet its approach is the <strong>polar opposite than it took four decades</strong> ago. Rather than operating in secret, it is building a collaborative network to develop quantum computing. The reason: the technology is simply too complex for anyone to go it alone.</p>
      <p>It’s not just IBM either. A recent report by Accenture Strategy found that found that ecosystems are <strong><i>increasingly seen as a “cornerstone” of future </i></strong>rowth. In fact, almost half of the executives it surveyed are actively seeking to participate in ecosystems to create new business models. Today, if you want to compete effectively, you need an ecosystem strategy.</p>
      <h2>Porter’s Competitive Advantage</h2>
      <p>The dominant view of strategy in the 20th century was based on Michael Porter’s ideas about competitive advantage. In essence, he argued that the key to long-term success was to dominate the value chain by maximizing bargaining power among suppliers, customers, new market entrants and substitute goods.</p>
      <p>Porter’s ideas dominated thinking in corporate strategy for decades, yet they had a fatal flaw that wasn’t always obvious. Thinking in terms of value chains is viable when technology is relatively static, but when the marketplace is rapidly evolving it can get you locked out of important ecosystems and greatly diminish your ability to compete.</p>
      <p>IBM’s history has shown a steady evolution from value chains to ecosystems. When it developed its System 360 mainframe architecture in the late 50s, the value chain was almost completely self-contained and vertically integrated. The PC was more of a hybrid strategy, in which it partnered with companies like Microsoft and Intel, but mostly kept them at arm’s length.</p>
